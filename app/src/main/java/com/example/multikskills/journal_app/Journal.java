package com.example.multikskills.journal_app;

public class Journal {

    public Journal() {
    }

    public Journal(String title, String message, String date) {
        this.title = title;
        this.message = message;
        this.date =date;


    }
    public Journal(String title,  String date, String message , String key) {
        this.title = title;
        this.message = message;
        this.date = date;
        this.key=key;

    }

    String title, message, date, key;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }



}



